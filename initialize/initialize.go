func InitServerLog() func() {
	file, err := logger.OpenLogFile(logger.ServerLevel)
	if err != nil {
		panic(err)
	}
	// defer file.Close()
	gin.DefaultWriter = io.MultiWriter(os.Stdout, file)
	gin.ForceConsoleColor()
	return func() {
		file.Close()
	}
}
